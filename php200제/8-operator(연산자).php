<?php
    //더하기
    $sum = 5+5; //5+5를 연산한 값이 변수 sum에 대입
    echo "<더하기><br>";
    echo "5더하기 5 는 ".$sum;
    echo "<br><br>";

    //빼기
    $minus = 5-3; // 5-3을 연산한 값이 변수 minus에 대입
    echo "<빼기><br>";
    echo "5빼기3은 ".$minus;
    echo "<br><br>";

    //곱하기
    $mul = 5*3; // 5*3을 연산한 값이 변수 mul에 대입
    echo "<곱하기><br>";
    echo "5곱하기3은 ".$mul;
    echo "<br><br>";

    //나누기
    $division = 6/3; // 6/3을 연산한 값이 변수 division에 대입
    echo "<나누기><br>";
    echo "6나누기3은 ".$division;
    echo "<br><br>";

    //나머지
    $rest = 5%3; // 5%3을 연산한 값이 변수 rest에 대입
    echo "<나머지><br>";
    echo "5나누기3의 나머지 값은 ".$rest;
?>